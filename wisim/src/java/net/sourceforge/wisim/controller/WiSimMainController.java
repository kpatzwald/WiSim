    private void disableButtons(JMenu menuWahl, String wahl)
    {
      if (JOptionPane.showConfirmDialog(this, "Möchten Sie in den Bereich " + wahl + " wechseln?", "Bereichswahl", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE) == 0)
      {
        menuWahl.setEnabled(true);
        jButtonEinkauf.setEnabled(false);
        jButtonLager.setEnabled(false);
        jButtonProduktion.setEnabled(false);
        jButtonRechnungswesen.setEnabled(false);
        jButtonVertrieb.setEnabled(false);
      }
    }
    
