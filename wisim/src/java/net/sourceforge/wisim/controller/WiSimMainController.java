    /** Wechselt die Panels
     * @param panel Neues Panel
     */    
    public void changePanel(String panel) {
        getContentPane().removeAll();
        
        //Update Plugable look and feel
        SwingUtilities.updateComponentTreeUI((JPanel)actions.get(panel));
        SwingUtilities.updateComponentTreeUI(jMenuBar1);
        
        getContentPane().add((JPanel)actions.get(panel), java.awt.BorderLayout.CENTER);
        getContentPane().validate();
        getContentPane().repaint();
    }
    
